package com.abelavusau.restfulapi.model;

import org.springframework.data.annotation.Id;

public class Resource {
	@Id
	private long id;
	private String name;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
